# Simple IF
age = 20

if age < 18:
    print("You are Minor")



# IF Else
age = 20

if age < 18:
    print("You are a minor.")
else:
    print("You are Major.")


# Else-IF
age = 20


if age < 18:
    print("You are a minor.")
elif age >= 18 and age < 60:
    print("You are an adult.")
else:
    print("You are a senior citizen.")


